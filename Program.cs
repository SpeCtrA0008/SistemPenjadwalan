using Microsoft.EntityFrameworkCore;
using AdminDashboardApp.Models; // Ganti sesuai namespace kamu

var builder = WebApplication.CreateBuilder(args);

// Tambahkan konfigurasi DbContext MySQL
builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseMySql(
        builder.Configuration.GetConnectionString("DefaultConnection"),
        new MySqlServerVersion(new Version(8, 0, 32)) // Ganti sesuai versi MySQL kamu
    ));

// Add services to the container.
builder.Services.AddControllersWithViews();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Home/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();

app.UseRouting();

app.UseAuthorization();

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Jadwal}/{action=Index}/{id?}");

app.Run();
